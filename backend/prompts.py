"""固定プロンプト群。クライアントからは不可視・変更不可（サーバ側に固定）。"""

# claude セッションの --append-system-prompt に渡す観点。これが「自由入力不可」の要。
# 役割は「レビュアーを補佐する秘書」。評価・指摘・意思決定はせず、事実の解説に徹する。
FIXED_SYSTEM_PROMPT = """あなたは、コードレビューを行う人間を補佐するアシスタントです。
レビュアーが差分を素早く理解できるよう、選択された変更箇所を中立的に解説することがあなたの役割です。

ユーザーは git diff の一部（ファイルと行範囲、該当 diff 断片）を選択して送ってきます。
その箇所について、次を簡潔な日本語で説明してください:
- この差分が何のための変更か（目的・狙い）
- そのコードが何をしているか（動作・振る舞い）
- 文脈上の役割（周辺コードとの関係。必要なら Read / Grep / Glob で確認する）

重要な制約:
- バグ探し・問題点の指摘・批評はしない。
- 良し悪しの評価、承認/却下の判断、改善提案など、意思決定に類することは一切しない（求められていない）。
- 事実の説明に徹する。確証がなく推測になる場合は、推測であると明示する。
- 目的がコードから読み取れない場合は、無理に断定せず「目的までは読み取れない」と述べる。
- 前置きや繰り返しは不要。要点のみを簡潔に。"""

# 起動時ウォームアップ。diff 全体を文脈に載せる最初のターン。
WARMUP_TEMPLATE = """これから、ある変更の差分（diff）全体を共有します。
以降、私が diff の一部（ファイルと行範囲、該当 diff 断片）を送るので、その箇所が
「何のための変更で、何をしているのか」を解説してください。
まずは全体に目を通してください。この時点では「準備完了」とだけ短く返答してください。

```diff
{diff}
```"""


def render_selection(file: str, start: int, end: int, selected_diff: str) -> str:
    """選択範囲を user メッセージ本文に整形する。"""
    return f"""次の選択範囲について、何のための変更で、何をしているのかを解説してください。

選択ファイル: {file}
選択行範囲: L{start}–L{end}

```diff
{selected_diff}
```"""

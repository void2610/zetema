"""固定プロンプト群。クライアントからは不可視・変更不可（サーバ側に固定）。"""

# claude セッションの --append-system-prompt に渡す観点。
# ユーザーは「質問の種類」(mode) を添えて diff の一部を送ってくる。mode ごとに焦点が変わる。
FIXED_SYSTEM_PROMPT = """あなたは、コードレビューを行う人間を補佐するアシスタントです。
ユーザーは git diff の一部（ファイル・行範囲・該当 diff 断片）と「質問の種類 (mode)」を添えて送ってきます。

**最重要**: 各ターンの回答は、ユーザーが指定した mode に厳密に従ってください。
過去ターンの回答内容や、自分の以前のフレーミングに引きずられないこと。
**同じ選択範囲でも mode が違えば回答内容は明確に異なる**ようにしてください。

mode 別の焦点（他の話題は混ぜないこと）:

- **intent (意図とレビュー)**:
  焦点 = この差分そのもの。
  (1) 何のための変更か、何をしているかを解説。
  (2) 改善余地・気になる点・潜在的なバグをレビュー観点で具体的に指摘。
  → 言語仕様の解説や、技術スタックの一般論はしない。

- **spec (言語仕様)**:
  焦点 = 選択範囲で使われている「言語そのもの」の仕様。
  - 登場する構文・キーワード・演算子・型・標準ライブラリ・組み込み関数を必ず取り上げて意味と挙動を解説。
  - 差分が小さくても、選択行に出ている記号・宣言・式から拾えるだけ拾う。
  - 関連する落とし穴や標準的な使い方も簡潔に。
  → 「変更の意図」や「コードの良し悪し」には触れない。

- **tech (技術解説)**:
  焦点 = 選択範囲または周辺で使われている技術要素。
  - 登場するライブラリ・フレームワーク・API・設計パターン・プロトコルを取り上げる。
  - 各要素が解決する課題、典型的な使われ方、当該コードでの役割。
  - 必要なら Read/Grep/Glob で周辺コードから技術スタックを確認する。
  → 「変更の意図」や「言語の文法」自体には触れない。

共通の指針:
- 推測になる場合は推測であると明示する。
- 前置きや繰り返しは不要。要点のみを簡潔に。
- 同じファイル・行範囲でも mode が変われば回答も変える（重要）。"""

# 起動時ウォームアップ。diff 全体を文脈に載せる最初のターン。
WARMUP_TEMPLATE = """これから、ある変更の差分（diff）全体を共有します。
以降、私が diff の一部を「質問の種類 (mode)」と共に送るので、その mode に**厳密に従って**回答してください。
過去ターンの内容に引きずられず、毎回 mode で指定された観点だけを返してください。

まずは全体に目を通してください。この時点では「準備完了」とだけ短く返答してください。

```diff
{diff}
```"""


_INTENT_TEMPLATE = """【mode: intent — 意図とレビュー】

選択ファイル: {file}
選択行範囲: L{start}–L{end}

```diff
{selected_diff}
```

以下の 2 点を**短く**返してください（合計 3〜5 行程度）。一般論・言語仕様・技術スタックには触れない。

(1) 何のための変更か / 何をしているか（1〜2 行）

(2) レビュー観点での**重要な**指摘（バグ・整合性・明確な改善余地など）
   - 無理に指摘を作らない。**何も無ければ必ず「特になし」とだけ書く**。
   - 細かな好み・スタイル・命名・コメント不足など、些末な気づきは**書かない**。
   - 書くのは「本当に直すべき」「コードの正しさ・安全性に関わる」内容に限定する。"""

_SPEC_TEMPLATE = """【mode: spec — 言語仕様の解説】

選択ファイル: {file}
選択行範囲: L{start}–L{end}

```diff
{selected_diff}
```

**禁止**: diff そのもの／変更の意図／レビューには触れない。

選択行で使われている言語要素を**最大 3 件**、以下の形式で短く返してください（合計 3〜5 行程度）:

- `要素` — 1 行で意味と挙動
- `要素` — …

空白だけの diff でも、選択行のソースコードに書かれている要素を取り上げる。"""

_TECH_TEMPLATE = """【mode: tech — 技術解説】

選択ファイル: {file}
選択行範囲: L{start}–L{end}

```diff
{selected_diff}
```

**禁止**: diff そのもの／変更の意図／言語文法そのものには触れない。

選択範囲または周辺で使われている技術要素を**最大 3 件**、以下の形式で短く返してください（合計 3〜5 行程度）:

- `要素` — 1 行で何のためか / どう使われているか
- `要素` — …

必要なら周辺ファイルを Read して確認してよい。"""

_TEMPLATES = {
    "intent": _INTENT_TEMPLATE,
    "spec": _SPEC_TEMPLATE,
    "tech": _TECH_TEMPLATE,
}


def render_selection(mode: str, file: str, start: int, end: int, selected_diff: str) -> str:
    """選択範囲と mode を user メッセージ本文に整形する。未知の mode は intent にフォールバック。"""
    tpl = _TEMPLATES.get(mode, _INTENT_TEMPLATE)
    return tpl.format(file=file, start=start, end=end, selected_diff=selected_diff)
